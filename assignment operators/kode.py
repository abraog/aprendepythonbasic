# Operador "="
x = 5   # x sai 5

# Operador "+="
x += 3  # x sai 8 (5 + 3)

# Operador "-="
x -= 2  # x sai 6 (8 - 2)

# Operador "*="
x *= 4  # x sai 24 (6 * 4)

# Operador "/="
x /= 3  # x sai 8.0 (24 / 3)

# Operador "//="
x //= 2 # x sai 4.0 (8.0 // 2)

# Operador "%="
x %= 3  # x sai 1.0 (4.0 % 3)

# Operador "&="
x &= 3  # x sai 0 (100 & 011 = 000)

# Operador "|="
x |= 6  # x sai 7 (000 | 110 = 111)

# Operador "^="
x ^= 5  # x sai 2 (111 ^ 101 = 010)

# Operador "<<="
x <<= 2 # x sai 8 (0010 << 2 = 1000)

# Operador ">>="
x >>= 1 # x sai 4 (1000 >> 1 = 0100)